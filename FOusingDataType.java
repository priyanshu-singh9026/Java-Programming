public class FOusingDataType {
    public static int sum(int a,int b){
        return a+b;
    }
    public static float sum(float a,float b){
        return a+b;
    }
    public static void main (String rgs[]){
        System.out.println(sum(4,3));
        System.out.println(sum(3.14f,4.16f));
    }
    
}
